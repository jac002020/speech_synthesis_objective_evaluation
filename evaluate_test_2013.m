    struct( ...  
        'name',                  'EH1_similarity',...
        'objective_resultfile',  'tests/2013/2013_EH1_objective_results_sim',...
        'testfilelist',          'tests/2013/2013_EH1_sim.test.scp',...
        'reffilelist',           'tests/2013/2013_EH1_sim.ref.scp',...
